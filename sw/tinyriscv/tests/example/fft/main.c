#define M_PI 3.14159265

#define MAX(x, y) (((x) > (y)) ? (x) : (y))

float exp_approx(float x) {
    float result = 1.0;  // 初始项1
    float term = 1.0;    // 当前项的值
    int n = 1;            // 阶乘的计数器
    
    // 我们不断累加项，直到当前项足够小为止
    while (term > 1e-15 || term < -1e-15) {
        term *= x / n;    // 计算当前项的值
        result += term;   // 将当前项加到结果中
        n++;              // 增加阶乘计数
    }
    
    return result;
}

// ReLU 激活函数
void ReLU(int size, float* input) {
    for (int i = 0; i < size; i++) {
        input[i] = MAX(0, input[i]);
    }
}

// Softmax 激活函数
void softmax(int size, float* input) {
    float max = input[0];
    for (int i = 1; i < size; i++) {
        if (input[i] > max) max = input[i];
    }

    float sum = 0.0;
    for (int i = 0; i < size; i++) {
        input[i] = exp_approx(input[i] - max);
        sum += input[i];
    }

    for (int i = 0; i < size; i++) {
        input[i] /= sum;
    }
}

// 矩阵乘法
void matmul(int m, int n, int p, float* A, float* B, float* C) {
    for (int i = 0; i < m; i++) {
        for (int j = 0; j < p; j++) {
            C[i * p + j] = 0.0;  // 使用一维数组模拟二维数组
            for (int k = 0; k < n; k++) {
                C[i * p + j] += A[i * n + k] * B[k * p + j];  // 计算一维数组的索引
            }
        }
    }
}

// 向量加法
void vector_add(int size, float* vec, float* bias, float* result) {
    for (int i = 0; i < size; i++) {
        result[i] = vec[i] + bias[i];
    }
}

// 前向传播
void FNN4Layers(float* input, float* W1, float* b1,
                float* W2, float* b2,
                float* W3, float* b3,
                int* output) {
    float f1[30], f2[20], final_output[2];

    // 第一层
    matmul(1, 32, 30, input, W1, f1);
    vector_add(30, f1, b1, f1);
    ReLU(30, f1);

    // 第二层
    matmul(1, 30, 20, f1, W2, f2);
    vector_add(20, f2, b2, f2);
    ReLU(20, f2);

    // 第三层
    matmul(1, 20, 2, f2, W3, final_output);
    vector_add(2, final_output, b3, final_output);
    ReLU(2, final_output);

    // Softmax
    softmax(2, final_output);

    // 选择最大的值的索引作为输出
    if (final_output[0] > final_output[1]) {
        *output = 0;  // 输出类别0
    } else {
        *output = 1;  // 输出类别1
    }
}



// 计算准确率
float calculate_accuracy(int* predicted, int* actual, int num_samples) {
    int correct = 0;
    for (int i = 0; i < num_samples; i++) {
        if (predicted[i] == actual[i]) {
            correct++;
        }
    }
    return (float)correct / num_samples;
}


